// Write a Java program to print all odd numbers between 1 to 10
package java_labwork;

public class ques1 {
    public static void main(String[] args) {
        for (int i = 1; i < 10; i++) {
            if (i % 2 != 0) {
                System.out.println(i);
                
            }
        }
    }
    
}
